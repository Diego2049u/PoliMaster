def explicacion_grado1():
    print("\n==============================")
    print(" EXPLICACIÓN: FUNCIÓN DE GRADO 1")
    print("==============================\n")

    print("Una función de grado 1 es una fórmula de la forma:\n")
    print("        f(x) = a·x + b\n")

    print("¿Qué significa cada parte?")
    print(" - 'x' es el número que tú eliges para evaluar la función.")
    print(" - 'a' es la *pendiente* de la recta.")
    print("     ➤ La pendiente indica qué tan inclinada está la línea.")
    print("       Por ejemplo:")
    print("         • Si a > 0 → la recta sube hacia la derecha.")
    print("         • Si a < 0 → la recta baja hacia la derecha.")
    print("         • Si a = 0 → no hay inclinación (recta horizontal).")
    print(" - 'b' es la *ordenada al origen*.")
    print("     ➤ Es el valor de f(x) cuando x = 0.")
    print("     ➤ Es donde la recta cruza el eje Y.\n")

    print("Idea clave:")
    print(" Una función de grado 1 siempre dibuja una *línea recta*.")
    print(" No tiene curvas, no tiene puntos máximos ni mínimos.\n")

    print("Ejemplo:")
    print(" Si tienes f(x) = 2x + 3:")
    print("     - Subirá 2 unidades por cada 1 que avances en x.")
    print("     - Cruza el eje Y en el punto (0, 3).")
    print()


def explicacion_grado2():
    print("\n==============================")
    print(" EXPLICACIÓN: FUNCIÓN DE GRADO 2")
    print("==============================\n")

    print("Una función de grado 2 es una fórmula de la forma:\n")
    print("        f(x) = a·x² + b·x + c\n")

    print("¿Qué significa cada letra?")
    print(" - 'x' sigue siendo el número que eliges.")
    print(" - 'a' es el término más importante.")
    print("     ➤ Determina la *curvatura* de la parábola.")
    print("         • Si a > 0 → la parábola abre hacia arriba.")
    print("         • Si a < 0 → abre hacia abajo.")
    print("     ➤ También controla qué tan 'abierta' o 'cerrada' es.")
    print("         • Si |a| es grande → la parábola es angosta.")
    print("         • Si |a| es pequeña → la parábola es más ancha.")
    print()
    print(" - 'b' afecta el desplazamiento y el punto del vértice.")
    print(" - 'c' es la ordenada al origen.")
    print("     ➤ Es el valor de la función cuando x = 0.")
    print("     ➤ Es el punto donde la parábola toca el eje Y.\n")

    print("Forma de la gráfica:")
    print(" Una función de grado 2 es una *parábola*.")
    print(" Tiene una curva suave y un único punto importante:")
    print("     ➤ *el vértice*, que puede ser un máximo o un mínimo.\n")

    print("Ejemplo:")
    print(" Si f(x) = x² - 4x + 3, su parábola abre hacia arriba y tiene un mínimo.")
    print()


def explicacion_grado3():
    print("\n==============================")
    print(" EXPLICACIÓN: FUNCIÓN DE GRADO 3")
    print("==============================\n")

    print("Una función de grado 3 tiene la forma:\n")
    print("        f(x) = a·x³ + b·x² + c·x + d\n")

    print("¿Qué aporta cada término?")
    print(" - 'a' es el término dominante.")
    print("     ➤ Controla cómo se comporta la gráfica en los extremos.")
    print("         • Si a > 0 → izquierda baja, derecha sube.")
    print("         • Si a < 0 → izquierda sube, derecha baja.")
    print("     ➤ También influye en cuán pronunciada es la curva.\n")

    print(" - 'b' y 'c' controlan la forma del tramo central.")
    print("     ➤ Ajustan la cantidad de giros y la forma de la curva.")
    print("     ➤ Gracias a ellos, la función puede tener:")
    print("         • 0 puntos de inflexión")
    print("         • 1 punto de inflexión")
    print("         • hasta 2 cambios de curvatura\n")

    print(" - 'd' es la ordenada al origen.")
    print("     ➤ Es el valor de f(x) cuando x = 0 (punto donde toca el eje Y).\n")

    print("Forma de la gráfica:")
    print(" Una función de grado 3 genera curvas más complejas que pueden:")
    print("     ✓ subir y luego bajar")
    print("     ✓ bajar y luego subir")
    print("     ✓ tener forma de 'S' suave\n")

    print("Ejemplo:")
    print(" f(x) = x³ - 3x tiene dos cambios de curvatura y forma de S.")
    print()
